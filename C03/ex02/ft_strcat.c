char	*ft_strcat(char *dest, char *src)
{
	unsigned int	i;
	int				b;

	b = 0;
	i = 0;
	while (dest[b])
		b++;
	while (src[i])
	{
		dest[b] = src[i];
		b++;
		i++;
	}
	dest[b] = 0;
	return (dest);
}
