#include <unistd.h>

int	ft_checkall(char tab[6][6]);

void	ft_printtab4(char tab[6][6])
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	while (i < 5)
	{
		while (j < 5)
		{
			write(1, &tab[i][j], 1);
			write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
		j = 1;
	}
}
